//main class
class Main {
    //main method
    public static void main(String[] args) {
        //make condition statement
        int x = 10;
        int y = 16;

        //if statement
        if (x > y) {
            System.out.println("x is greater than y");
        } 
        if(x < y){
            System.out.println("x is less than y");
        }
        if(x == y){
            System.out.println("x is equal to y");
        }

    }
}